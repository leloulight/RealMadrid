KAS YRA SLAPUKAI?

Slapukai (cookies) yra mažos apimties tekstinės bylos, saugančios nedidelį kiekį informacijos. Jos yra siunčiamos į Jūsų kompiuterį, išmanųjį telefoną, ar kitą įrenginį tuo metu, kai Jūs apsilankote internetinėje svetainėje. Vėliau slapukai yra siunčiami iš Jūsų kompiuterio į tą pačią, ar kitą tą patį slapuką atpažįstančia svetainę kiekvieno apsilankymo metu. Tokiu būdu slapukai padeda svetainei atpažinti vartotojo įrenginį kiekvieno apsilankymo metu.

